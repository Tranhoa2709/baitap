    private void addPhieuNoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPhieuNoBtnActionPerformed
        PhieuNo phieuNo = getPhieuNoDataFromFields();
        if (phieuNo != null) {
            if (validatePhieuNo(phieuNo)) {
                phieuNoController.insert(phieuNo);
                showDanhSachPhieuNo();
                clearPhieuNoFields();
                JOptionPane.showMessageDialog(this, "Thêm phieu no thanh cong. ");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Xin hãy điền đầy đủ thông phiếu nợ.");
        }
    }//GEN-LAST:event_addPhieuNoBtnActionPerformed

    private void editPhieuNoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editPhieuNoBtnActionPerformed
        if (selectedPhieuNoID == -1) {
            int selectedIndex = PhieuNoTbl.getSelectedRow();
            if (selectedIndex >= 0) {
                int phieuNoID = Integer.parseInt(PhieuNoTbl.getValueAt(selectedIndex, 0).toString());
                PhieuNo selectedPhieuNo = phieuNoController.getOne(phieuNoID);
                selectedPhieuNoID = phieuNoID;
                fillPhieuNoFields(selectedPhieuNo);
            } else {
                JOptionPane.showMessageDialog(this, "Xin hãy chọn hàng của phiếu nợ muốn sửa.");
            }
        } else {
            int option = JOptionPane.showConfirmDialog(this, "Bạn có muốn lưu lại thay đổi không? ");
            if (option == 0) {
                PhieuNo editedPhieuNo = getPhieuNoDataFromFields();
                if (editedPhieuNo != null) {
                    if (validatePhieuNo(editedPhieuNo)) {
                        phieuNoController.update(selectedPhieuNoID, editedPhieuNo);
                        selectedPhieuNoID = -1;
                        clearPhieuNoFields();
                        showDanhSachPhieuNo();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Xin hãy điền đầy đủ thông tin của phiếu nợ.");
                }
            }
        }
    }//GEN-LAST:event_editPhieuNoBtnActionPerformed

    private void deletePhieuNoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletePhieuNoBtnActionPerformed
        if (selectedPhieuNoID == -1) {
            int selectedIndex = PhieuNoTbl.getSelectedRow();
            if (selectedIndex >= 0) {
                int phieuNoID = Integer.parseInt(PhieuNoTbl.getValueAt(selectedIndex, 0).toString());
                selectedPhieuNoID = phieuNoID;
            } else {
                JOptionPane.showMessageDialog(this, "Xin hãy chọn hàng của phiếu nợ muốn sửa.");
            }
        } else {
            int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không ? ");
            if (option == 0) {
                phieuNoController.delete(selectedPhieuNoID);
                selectedPhieuNoID = -1;
                showDanhSachPhieuNo();
                JOptionPane.showMessageDialog(this, "Xóa thành công.");
            }
        }
    }//GEN-LAST:event_deletePhieuNoBtnActionPerformed

