    // Hiển thị danh sách phiếu nợ ra bảng
    private void showDanhSachPhieuNo() {
        List<PhieuNo> listPhieuNo = phieuNoController.getList();
        phieuNoModel.setRowCount(0);
        listPhieuNo.forEach((phieuNo) -> {
            phieuNoModel.addRow(new Object[]{
                phieuNo.getId(),
                phieuNo.getMaSV(),
                phieuNo.getSoTienConNo(),
                phieuNo.getNgayDaoHan(),});
        });
    }

    // Lấy thông tin phiếu nợ từ các trường nhập
    private PhieuNo getPhieuNoDataFromFields() {
        String maSV = noTienMaSVField.getText();
        String ngayDaoHan = noTienNgayDaoHanField.getText();
        String soTienNoStr = noTienSoTienNoField.getText();
        if (!maSV.isEmpty() && !ngayDaoHan.isEmpty() && !soTienNoStr.isEmpty()) {
            int soTienNo = Integer.parseInt(soTienNoStr);
            PhieuNo phieuNo = new PhieuNo(maSV, soTienNo, ngayDaoHan);
            return phieuNo;
        }
        return null;
    }

    // Điền thông tin phiếu nợ vào các ô nhập (đối với edit)
    private void fillPhieuNoFields(PhieuNo phieuNo) {
        noTienMaSVField.setText(phieuNo.getMaSV());
        noTienNgayDaoHanField.setText(phieuNo.getNgayDaoHan());
        noTienSoTienNoField.setText(phieuNo.getSoTienConNo() + "");
    }

    /**
     * Kiểm tra thông tin phiếu nợ ở đây chúng ta cũng chỉ kiểm tra xem có tồn
     * tại sinh viên không.
     */
    private boolean validatePhieuNo(PhieuNo phieuNo) {
        SinhVien sv = sinhVienController.findByMaSV(phieuNo.getMaSV());
        if (sv == null) {
            JOptionPane.showMessageDialog(this, "Không tồn tại sinh viên này. Xin hãy kiểm tra lại.");
            return false;
        }
        return true;
    }

    // Xóa thông tin ở các trường nhập phiếu nợ
    private void clearPhieuNoFields() {
        noTienMaSVField.setText("");
        noTienNgayDaoHanField.setText("");
        noTienSoTienNoField.setText("");
    }

